// Populate options di select
function populateOptions() {
  const kodeSelect = document.getElementById("kodeBarang");
  const namaSelect = document.getElementById("namaBarang");

  kodeSelect.length = 1; // hapus opsi sebelumnya, sisakan placeholder
  namaSelect.length = 1;

  for (const item of barangData) {
    const optKode = document.createElement("option");
    optKode.value = item.kode;
    optKode.textContent = item.kode;
    kodeSelect.appendChild(optKode);

    const optNama = document.createElement("option");
    optNama.value = item.nama;
    optNama.textContent = item.nama;
    namaSelect.appendChild(optNama);
  }
}

document.addEventListener("DOMContentLoaded", () => {
  populateOptions();

  let isSyncing = false;

  $("#kodeBarang").on("change", function () {
    if (isSyncing) return;
    isSyncing = true;

    const kode = this.value;
    const data = barangData.find((item) => item.kode === kode);
    if (data) {
      $("#namaBarang").val(data.nama).trigger("change");
      $("#harga").val(data.harga);
    } else {
      $("#namaBarang").val(null).trigger("change");
      $("#harga").val("");
    }

    setTimeout(() => {
      $("#namaBarang").select2("close");
      $("#kodeBarang").select2("close");
      isSyncing = false;
    }, 0);
  });

  $("#namaBarang").on("change", function () {
    if (isSyncing) return;
    isSyncing = true;

    const nama = this.value;
    const kode = namaKodeMap[nama];
    const data = barangData.find((item) => item.nama === nama);
    if (data) {
      $("#kodeBarang").val(kode).trigger("change");
      $("#harga").val(data.harga);
    } else {
      $("#kodeBarang").val(null).trigger("change");
      $("#harga").val("");
    }

    setTimeout(() => {
      $("#kodeBarang").select2("close");
      $("#namaBarang").select2("close");
      $("#qty").focus();
      isSyncing = false;
    }, 0);
  });
});
